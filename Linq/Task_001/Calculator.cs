namespace Linq
{
    public class Calculator
    {
        public dynamic Add(dynamic a, dynamic b)
        {
            return a + b;
        }
        
        public dynamic Sub(dynamic a, dynamic b)
        {
            return a - b;
        }
        
        public dynamic Multiply(dynamic a, dynamic b)
        {
            return a * b;
        }
        
        public dynamic Div(dynamic a, dynamic b)
        {
            return a / b;
        }
    }
}